package networking;

/**
 * A Data Structure that keeps a tuple: An index and a type
 * @author Carlos Vasquez
 *
 */
public class Tuple
{
	/******************* Class Attributes *******************/
	public final int index;
	public final int type;
	
	/******************* Class Constants *******************/
	public Tuple(int index, int type)
	{
		this.index = index;
		this.type = type;
		
	} /* end Constructor */
	
} /* end Triple */
